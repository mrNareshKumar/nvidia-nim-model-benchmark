const colorCache = {};

export function getProvider(id) {
  const lower = id.toLowerCase();
  for (const [prefix, name] of Object.entries(PROVIDER_MAP)) {
    if (lower.startsWith(prefix)) return name;
  }
  if (lower.includes('/')) return lower.split('/')[0];
  return 'Other';
}

export function getProviderColor(provider) {
  if (!colorCache[provider]) {
    const i = Object.keys(colorCache).length % PROVIDER_COLORS.length;
    colorCache[provider] = PROVIDER_COLORS[i];
  }
  return colorCache[provider];
}

export function getAllProviders(data) {
  return [...new Set(data.map(d => getProvider(d.id)))].sort();
}

